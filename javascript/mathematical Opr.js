class MathematicalOperation{
    constructor(){

    }

    additionOperation(a,b,c){
        return (a+b+c);
    }

    subtrationOperation(a,b,c){
        return (a-b-c);
    }

    multiplicationOperation(a,b,c){
        return (a*b*c);
    }

    divisionOperation(a,b,c){
        return (a/b/c);
    }

}